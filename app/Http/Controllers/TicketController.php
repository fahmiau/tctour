     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ticketDetail = new TicketDetail;
        $ticketDetail->depCity = $request->depCity;
        $ticketDetail->arrCity = $request->arrCity;
        $ticketDetail->pergi = $request->pergi;
        $ticketDetail->pulang = $request->pulang;
        $ticketDetail->seatClass = $request->seatClass;
        $ticketDetail->price = $request->price;

        $ticketDetail->save();
        return redirect('/admin/ticket');
    }

